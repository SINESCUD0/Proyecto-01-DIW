function w3_open() {
  document.getElementById("mySidebar").style.display = "block";
  document.getElementById("texto").style.marginLeft = "210px";
  document.getElementById("titulo").style.marginLeft = "210px";
}

function w3_close() {
  document.getElementById("mySidebar").style.display = "none";
  document.getElementById("texto").style.marginLeft = "0px";
  document.getElementById("titulo").style.marginLeft = "0px";
}
